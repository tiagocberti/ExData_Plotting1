#Read the data
data <- read.table("household_power_consumption.txt", skip = 66637, nrow = 2880, sep = ";")
#Convert names
name <- sapply(read.table("household_power_consumption.txt", nrow = 1, sep = ";"), as.character)
names(data) <- name
#Convert data formats
data$DateTime <- strptime(paste(data$Date, data$Time), format="%d/%m/%Y %H:%M:%S")
for( i in 3:9 ){
  data[[i]] <- sapply(data[[i]], as.character)
  data[[i]] <- sapply(data[[i]], as.numeric)
}
#Generate graphics
png(filename = "plot3.png",width = 480, height = 480, units = "px")
with(data, plot(DateTime, Sub_metering_1, type = "n", xlab = "daytime", ylab = "Energy sub metering" , axes = F, frame.plot=TRUE))
axis(1, at=c(as.numeric(min(data$DateTime)), as.numeric(min(data$DateTime))+86400
             , as.numeric(min(data$DateTime))+2*86400), labels=c("Thu", "Fri", "Sat"))
axis(2, yaxs = "r")
with(data, points(DateTime, Sub_metering_1, type = "l"))
with(data, points(DateTime, Sub_metering_2, type = "l", col = "red"))
with(data, points(DateTime, Sub_metering_3, type = "l", col = "blue"))
legend("topright", pch = 151, col = c("black", "red", "blue"), legend = c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"))
dev.off()
